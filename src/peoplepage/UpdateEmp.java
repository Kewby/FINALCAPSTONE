    public UpdateEmp(String name, int empId, String firstname, String lastname, String username, String email, String contact, String address, String role) {
        initComponents();
        EmployeeModel em = new EmployeeModel();
        ResultSet rs = null; //em.update;
        int ndx, val;
        val = 0;
        this.setLocationRelativeTo(null);
        setName(name);
        String password = "";
        String confirm = "";
        setEmpId(empId);
        System.out.println("Employee ID:"+empId);
        
        txtFirstname.setText(firstname);
        txtLastname.setText(lastname);
        txtUsername.setText(username);
        txtPassword.setText(password);
        txtConfirm.setText(confirm);
        txtEmail.setText(email);
        txtContactNumber.setText(contact);
        txtAddress.setText(address);
        
        rs = uec.getRole();
        try{
            for (ndx = 0; rs.next(); ndx++){
                comboRole.addItem(rs.getString(1));  
                if(role.compareToIgnoreCase(rs.getString(1))==0){
                    val = ndx;
                }
            }
        } catch (SQLException ex){
             Logger.getLogger(UpdateEmp.class.getName()).log(Level.SEVERE, null, ex);
        }       
    }
